char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (to_find[i + 1] == '\0')
				return (str + 1);
			j++;
		}
	i++;
	}
	return (0);
}
